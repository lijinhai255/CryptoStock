"use client";

import { useState, useEffect } from "react";
import {
  X,
  TrendingUp,
  TrendingDown,
  AlertCircle,
  Wallet,
  Loader2,
  CheckCircle,
} from "lucide-react";
import { formatUnits, parseUnits } from "viem";
import { Button } from "@/components/ui/button";
import { useToast } from "@/hooks/use-toast";
import { PriceSourceIndicator } from "@/components/PriceSourceIndicator";
import useTokenTrading from "@/lib/hooks/useTokenTrading";

// È¢ÑËÆæÈáëÈ¢ùÈÄâÈ°π
const PRESET_AMOUNTS = [10, 50, 100, 500, 1000, 5000];

// ÊªëÁÇπÈÄâÈ°π
const SLIPPAGE_OPTIONS = [
  { label: "3%", value: 3 },
  { label: "5%", value: 5 },
  { label: "10%", value: 10 },
  { label: "15%", value: 15 },
  { label: "Ëá™ÂÆö‰πâ", value: "custom" },
];

interface BuyModalProps {
  isOpen: boolean;
  onClose: () => void;
  token: {
    symbol: string;
    name: string;
    price: string;
    change24h: number;
    volume24h: number;
    marketCap: number;
    address: `0x${string}`;
  };
  oracleAddress: `0x${string}`;
  usdtAddress: `0x${string}`;
}

export default function BuyModal({
  isOpen,
  onClose,
  token,
  oracleAddress,
  usdtAddress,
}: BuyModalProps) {
  const { toast } = useToast();

  // ËΩ¨Êç¢ token Êï∞ÊçÆÊ†ºÂºè
  const tokenInfo = {
    symbol: token.symbol,
    name: token.name,
    address: token.address,
    price: parseFloat(token.price.replace(/[$,]/g, "")),
    change24h: token.change24h,
    volume24h: token.volume24h,
    marketCap: token.marketCap,
    totalSupply: 0, // ÊöÇÊó∂‰ΩøÁî®ÈªòËÆ§ÂÄº
    userBalance: 0, // ÊöÇÊó∂‰ΩøÁî®ÈªòËÆ§ÂÄº
    userValue: 0, // ÊöÇÊó∂‰ΩøÁî®ÈªòËÆ§ÂÄº
  };

  // ‰ΩøÁî®Êñ∞ÁöÑ trading hook
  const {
    tradingState,
    isConnected,
    initializeData,
    approveUSDT,
    buyTokens,
    resetState,
    updateState,
  } = useTokenTrading(tokenInfo, usdtAddress, oracleAddress);

  const [showCustomSlippage, setShowCustomSlippage] = useState(false);

  const isPositive = token.change24h >= 0;

  // ÂàùÂßãÂåñÊï∞ÊçÆ
  useEffect(() => {
    if (isOpen && isConnected) {
      initializeData();
    }
  }, [isOpen, isConnected, initializeData]);

  // ÁõëÂê¨‰ª∑Ê†ºÊï∞ÊçÆÊõ¥Êñ∞ÔºåÂÆûÊó∂Âà∑Êñ∞ÂºπÁ™óÊòæÁ§∫
  useEffect(() => {
    if (isOpen && tradingState.priceData) {
      console.log("üí∞ ‰ª∑Ê†ºÊï∞ÊçÆÊõ¥Êñ∞ÔºåÂà∑Êñ∞ÂºπÁ™óÊòæÁ§∫:", {
        price: tradingState.priceData.price,
        lastUpdate: tradingState.updateData ? "ÊúâÊõ¥Êñ∞" : "Êó†Êõ¥Êñ∞"
      });
    }
  }, [isOpen, tradingState.priceData, tradingState.updateData]);

  // ÈáçÁΩÆÁä∂ÊÄÅÂΩìÊ®°ÊÄÅÊ°ÜÂÖ≥Èó≠Êó∂
  useEffect(() => {
    if (!isOpen) {
      resetState();
      setShowCustomSlippage(false);
    }
  }, [isOpen, resetState]);

  // Â§ÑÁêÜÊéàÊùÉ
  const handleApprove = async () => {
    const result = await approveUSDT();

    if (result.success) {
      toast({
        title: "ÊéàÊùÉÊàêÂäü",
        description: "USDTÊéàÊùÉÊàêÂäüÔºåÁé∞Âú®ÂèØ‰ª•Ë¥≠‰π∞‰ª£Â∏Å‰∫Ü",
      });
    } else {
      toast({
        title: "ÊéàÊùÉÂ§±Ë¥•",
        description: result.error || "ÊéàÊùÉÂ§±Ë¥•ÔºåËØ∑ÈáçËØï",
        variant: "destructive",
      });
    }
  };

  // Â§ÑÁêÜ‰π∞ÂÖ•
  const handleBuy = async () => {
    console.log("üöÄ ÂºÄÂßãË¥≠‰π∞‰ª£Â∏Å:", {
      token: token.symbol,
      tradingState,
      isConnected,
    });
    const result = await buyTokens();

    if (result.success) {
      toast({
        title: "Ë¥≠‰π∞ÊàêÂäü",
        description: `${token.symbol} Ë¥≠‰π∞ÊàêÂäüÔºÅ`,
      });
      setTimeout(() => {
        onClose();
      }, 2000);
    } else {
      toast({
        title: "Ë¥≠‰π∞Â§±Ë¥•",
        description: result.error || "Ë¥≠‰π∞Â§±Ë¥•ÔºåËØ∑ÈáçËØï",
        variant: "destructive",
      });
    }
  };

  // ËÆ°ÁÆóÊåâÈíÆÁä∂ÊÄÅ
  const getButtonState = () => {
    if (tradingState.transactionStatus === "approving") {
      return {
        text: "ÊéàÊùÉ‰∏≠...",
        disabled: true,
        color: "bg-yellow-500",
        icon: <Loader2 className="w-4 h-4 animate-spin" />,
      };
    }

    if (tradingState.transactionStatus === "buying") {
      return {
        text: "Ë¥≠‰π∞‰∏≠...",
        disabled: true,
        color: "bg-green-500",
        icon: <Loader2 className="w-4 h-4 animate-spin" />,
      };
    }

    if (tradingState.transactionStatus === "success") {
      return {
        text: "‰∫§ÊòìÊàêÂäü",
        disabled: true,
        color: "bg-green-500",
        icon: <CheckCircle className="w-4 h-4" />,
      };
    }

    if (!isConnected) {
      return {
        text: "ËøûÊé•Èí±ÂåÖ",
        disabled: false,
        color: "bg-blue-500",
        icon: <Wallet className="w-4 h-4" />,
      };
    }

    if (tradingState.needsApproval) {
      return {
        text: `ÊéàÊùÉ ${tradingState.buyAmount} USDT`,
        disabled:
          !tradingState.buyAmount || parseFloat(tradingState.buyAmount) <= 0,
        color: "bg-yellow-500",
        icon: null,
      };
    }

    return {
      text: `‰π∞ÂÖ• ${token.symbol}`,
      disabled:
        !tradingState.buyAmount ||
        parseFloat(tradingState.buyAmount) <= 0 ||
        tradingState.usdtBalance < parseUnits(tradingState.buyAmount, 6),
      color: "bg-green-500",
      icon: null,
    };
  };

  const buttonState = getButtonState();

  if (!isOpen) return null;

  return (
    <div className="fixed inset-0 bg-black/80 backdrop-blur-sm flex items-center justify-center z-50 p-4">
      <div className="bg-gray-900 rounded-2xl w-full max-w-md border border-gray-800 shadow-2xl">
        {/* Header */}
        <div className="flex items-center justify-between p-6 border-b border-gray-800">
          <div className="flex items-center gap-3">
            <div
              className={`w-12 h-12 rounded-xl flex items-center justify-center ${
                isPositive
                  ? "bg-gradient-to-br from-green-500 to-emerald-600"
                  : "bg-gradient-to-br from-red-500 to-orange-600"
              }`}
            >
              <span className="text-white font-bold text-lg">
                {token.symbol.charAt(0)}
              </span>
            </div>
            <div>
              <h3 className="text-white font-semibold text-lg">
                {token.symbol}
              </h3>
              <p className="text-gray-400 text-sm">{token.name}</p>
            </div>
          </div>
          <button
            onClick={onClose}
            className="text-gray-400 hover:text-white transition-colors"
          >
            <X className="w-5 h-5" />
          </button>
        </div>

        {/* Content */}
        <div className="p-6 space-y-6">
          {/* Price Info */}
          <div className="bg-gray-800/50 rounded-xl p-4">
            <div className="flex items-center justify-between mb-2">
              <span className="text-gray-400 text-sm">ÂΩìÂâç‰ª∑Ê†º</span>
              <PriceSourceIndicator source="fallback" />
            </div>
            <div className="flex items-center justify-between">
              <span className="text-white text-2xl font-bold">
                {token.price}
              </span>
              <div
                className={`flex items-center gap-1 px-2 py-1 rounded-lg ${
                  isPositive
                    ? "bg-green-500/20 text-green-400"
                    : "bg-red-500/20 text-red-400"
                }`}
              >
                {isPositive ? (
                  <TrendingUp className="w-4 h-4" />
                ) : (
                  <TrendingDown className="w-4 h-4" />
                )}
                <span className="text-sm font-semibold">
                  {isPositive ? "+" : ""}
                  {token.change24h.toFixed(2)}%
                </span>
              </div>
            </div>
          </div>

          {/* Balance */}
          <div className="flex items-center justify-between text-sm">
            <span className="text-gray-400">USDT ‰ΩôÈ¢ù</span>
            <span className="text-white">
              {formatUnits(tradingState.usdtBalance, 6)} USDT
            </span>
          </div>

          {/* Amount Input */}
          <div>
            <label className="block text-gray-400 text-sm mb-2">
              Ë¥≠‰π∞ÈáëÈ¢ù (USDT)
            </label>
            <div className="flex gap-2 mb-3">
              {PRESET_AMOUNTS.map((amount) => (
                <button
                  key={amount}
                  onClick={() => updateState({ buyAmount: amount.toString() })}
                  className={`flex-1 py-2 px-3 rounded-lg border transition-all ${
                    tradingState.buyAmount === amount.toString()
                      ? "border-blue-500 bg-blue-500/20 text-blue-400"
                      : "border-gray-700 text-gray-400 hover:border-gray-600"
                  }`}
                >
                  ${amount}
                </button>
              ))}
            </div>
            <input
              type="number"
              value={tradingState.buyAmount}
              onChange={(e) => updateState({ buyAmount: e.target.value })}
              placeholder="ËæìÂÖ•ÈáëÈ¢ù"
              className="w-full bg-gray-800 border border-gray-700 rounded-lg px-4 py-3 text-white focus:border-blue-500 focus:outline-none"
            />
          </div>

          {/* Slippage */}
          <div>
            <label className="block text-gray-400 text-sm mb-2">
              ÊªëÁÇπÂÆπÂøçÂ∫¶
            </label>
            <div className="flex gap-2 mb-3">
              {SLIPPAGE_OPTIONS.map((option) => (
                <button
                  key={option.value}
                  onClick={() => {
                    if (option.value === "custom") {
                      setShowCustomSlippage(true);
                    } else {
                      updateState({
                        slippage: typeof option.value === 'number' ? option.value : 5,
                        customSlippage: "",
                      });
                      setShowCustomSlippage(false);
                    }
                  }}
                  className={`flex-1 py-2 px-3 rounded-lg border transition-all text-sm ${
                    (typeof option.value === 'number' && tradingState.slippage === option.value) ||
                    (option.value === "custom" && showCustomSlippage)
                      ? "border-blue-500 bg-blue-500/20 text-blue-400"
                      : "border-gray-700 text-gray-400 hover:border-gray-600"
                  }`}
                >
                  {option.label}
                </button>
              ))}
            </div>
            {showCustomSlippage && (
              <input
                type="number"
                value={tradingState.customSlippage}
                onChange={(e) => {
                  const value = e.target.value;
                  updateState({
                    customSlippage: value,
                    slippage: value ? parseFloat(value) : 5
                  });
                }}
                placeholder="Ëá™ÂÆö‰πâÊªëÁÇπ %"
                className="w-full bg-gray-800 border border-gray-700 rounded-lg px-4 py-3 text-white focus:border-blue-500 focus:outline-none"
              />
            )}
          </div>

          {/* Transaction Status */}
          {tradingState.transactionStatus === "error" && (
            <div className="bg-red-500/10 border border-red-500/20 rounded-lg p-4">
              <div className="flex items-center gap-2 text-red-400">
                <AlertCircle className="w-5 h-5" />
                <span className="text-sm">‰∫§ÊòìÂ§±Ë¥•ÔºåËØ∑ÈáçËØï</span>
              </div>
            </div>
          )}

          {tradingState.transactionStatus === "success" && (
            <div className="bg-green-500/10 border border-green-500/20 rounded-lg p-4">
              <div className="flex items-center gap-2 text-green-400">
                <CheckCircle className="w-5 h-5" />
                <span className="text-sm">‰∫§ÊòìÊàêÂäüÔºÅ</span>
              </div>
            </div>
          )}
        </div>

        {/* Footer */}
        <div className="p-6 border-t border-gray-800">
          <Button
            onClick={tradingState.needsApproval ? handleApprove : handleBuy}
            disabled={buttonState.disabled}
            className={`w-full py-3 rounded-lg font-semibold text-white transition-all ${
              buttonState.disabled
                ? "opacity-50 cursor-not-allowed"
                : "hover:opacity-90"
            } ${buttonState.color}`}
          >
            <div className="flex items-center justify-center gap-2">
              {buttonState.icon}
              {buttonState.text}
            </div>
          </Button>

          {!isConnected && (
            <p className="text-center text-gray-400 text-sm mt-3">
              ËØ∑ÂÖàËøûÊé•Èí±ÂåÖ‰ª•ÁªßÁª≠‰∫§Êòì
            </p>
          )}
        </div>
      </div>
    </div>
  );
}
